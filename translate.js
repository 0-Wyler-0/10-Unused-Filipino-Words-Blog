
let toggleTranslate1Status = false;

let toggleTranslate1 = function() {
    let getTranslate1 = document.querySelector(".translator-1");
    let getFilipino1 = document.querySelector("#def-1");

    if (toggleTranslate1Status === false) {
        
        getTranslate1.innerHTML = "Translate to Filipino";
        getFilipino1.innerHTML = "A system for sending messages from one computer to another";

        toggleTranslate1Status = true;
    }
    else {
        toggleTranslate1Status = false
        getTranslate1.innerHTML = "Translate to English";
        getFilipino1.innerHTML = "Isang proseso ng pagpapasa o pagbibigay ng mensahe patungkol sa impormasyong mayron ka.";
    }
}

